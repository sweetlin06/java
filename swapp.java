public class swapp{

    public static void main(String[]args){
        int x=45,y=87;
        System.out.println("before swapping");
        System.out.println("x:"+x);
        System.out.println("y:"+y); 
        int temp=x;
        x=y;
        y=temp;
         System.out.println("after swapping");
        System.out.println("x:"+x);
        System.out.println("y:"+y); 
       
    }
}

